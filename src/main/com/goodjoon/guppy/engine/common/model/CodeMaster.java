package com.goodjoon.guppy.engine.common.model;


public class CodeMaster extends BaseModel {
	protected String codeGroup;
	protected String codeName;
	protected String codeOption;
	protected String codeValue;
	protected String displayName;
	protected String description;
	
	public String getCodeGroup() {
		return codeGroup;
	}
	public void setCodeGroup(String codeGroup) {
		this.codeGroup = codeGroup;
	}
	public String getCodeName() {
		return codeName;
	}
	public void setCodeName(String codeName) {
		this.codeName = codeName;
	}
	public String getCodeOption() {
		return codeOption;
	}
	public void setCodeOption(String codeOption) {
		this.codeOption = codeOption;
	}
	public String getCodeValue() {
		return codeValue;
	}
	public void setCodeValue(String codeValue) {
		this.codeValue = codeValue;
	}
	public String getDisplayName() {
		return displayName;
	}
	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}

	
}
